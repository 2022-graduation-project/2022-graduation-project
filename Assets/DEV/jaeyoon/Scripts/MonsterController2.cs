    /*---------------------------------------------
     *              UPDATEHPBAR
     * -------------------------------------------*/

    public void UpdateHpBar(float hp)
    {
        hpBarImage.fillAmount = hp / monsterData.maxHp;
        if (hp <= 0f)
        {
            hpBarImage.GetComponentsInParent<Image>()[1].color = Color.clear;
        }
