                //???? ????
                //?????? ?????? Attack ?????? ????
                monsterData.state = MonsterData.States.Chase;
                StartCoroutine(Chase());
                yield break;
            }

            //???? ???? 2.5?? ???????? ????
            if (attackTime >= 2.5f)
            {
                //timer reset
                attackTime = 0f;

                //Start Attack
                //???? ?????????? ???? ?? (chase ?? || ???? collider ?????? ???? ??)
                if (monsterData.destPosition != null)
                {
                    // ???????? HP ????
                    // damaging player
                    Invoke("damaging", 0.83f);

                    Animating();
                }
            }
            else anim.SetBool("isIdle", true);
            //Wait until next frame
            yield return null;
        }

        // ???? ?????????? ????????.
        yield return null;
    }

